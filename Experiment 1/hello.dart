void main() {
  // Variables
  var name = "Flutter";
  int version = 3;

  // Print statements
  print("Hello, $name!");
  print("Dart version: $version");

  // Conditional
  if (version >= 3) {
    print("You are using the latest Dart.");
  } else {
    print("Update Dart for latest features.");
  }

  // Loop
  for (int i = 1; i <= 5; i++) {
    print("Count: $i");
  }
}
